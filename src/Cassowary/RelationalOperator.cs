namespace Cassowary;

/// <summary>
/// The possible relations that a constraint can specify.
/// </summary>
public enum RelationalOperator
{
    /// <summary>
    /// Less or equal to.
    /// </summary>
    LessThanOrEqual,

    /// <summary>
    /// Equal to.
    /// </summary>
    Equal,

    /// <summary>
    /// Greater or equal to.
    /// </summary>
    GreaterThanOrEqual
}
